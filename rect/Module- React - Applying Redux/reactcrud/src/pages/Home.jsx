 function Home() {
  const dispatch = useDispatch();
  let navigate = useNavigate();
  const { users } = useSelector((state) => state.data);
  
  useEffect(() => {
    dispatch(loadusers());
  }, [dispatch]);

  const handleDelete = (id)=>{
    if(window.confirm("are you sure wantend to dlete the user?")){
      dispatch(Deleteuser(id));
    }
 };
  return (
    <div>
    <div className={buttonClasses.root}>
    <Button onClick={()=>navigate("/adduser")} variant="contained" style={{marginRight:"5px"}} color="primary">Add user</Button></div>
      <Box
        sx={{
          display: "flex",
          flexDirection: "column",
          alignItems: "center",
          "& > *": {
            m: 1,
          },
        }}
      ></Box>
      <TableContainer component={Paper} style={{ marginTop: "80px" }}>
        <Table sx={{ minWidth: 700 }} aria-label="customized table">
          <TableHead>
            <TableRow>
              <StyledTableCell align="center">Name</StyledTableCell>
              <StyledTableCell align="center">Email</StyledTableCell>
              <StyledTableCell align="center">Contac</StyledTableCell>
              <StyledTableCell align="center">Adress</StyledTableCell>
              <StyledTableCell align="center">Action</StyledTableCell>
            </TableRow>
          </TableHead>
          <TableBody>
            {users &&
              users.map((user) => (
                <StyledTableRow key={user.id}>
                  <StyledTableCell component="th" scope="row">
                    {user.name}
                  </StyledTableCell>
                  <StyledTableCell align="center">{user.email}</StyledTableCell>
                  <StyledTableCell align="center">
                    {user.contac}
                  </StyledTableCell>
                  <StyledTableCell align="center">
                    {user.address}
                  </StyledTableCell>
                  <StyledTableCell align="center">
                    <ButtonGroup variant="text" aria-label="Basic button group">
                      <Button style={{marginRight:"5px"}}  color="secondary" onClick={()=>handleDelete(user.id)}>Delete</Button>
                      <Button  style={{marginRight:"5px"}}   color="primary" onClick={() =>navigate(`/edit/${user.id}`)}>Edit</Button>
                    </ButtonGroup>
                  </StyledTableCell>
                </StyledTableRow>
              ))}
          </TableBody>
        </Table>
      </TableContainer>
    </div>
  );
}

export default Home;
